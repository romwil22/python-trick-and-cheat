# Dictionary - Update Method

student_info = {'name': 'rom', 'year': '4th',
                'studentnumber': '2017-00128-MN-0'}
print(student_info)

# to update student info dictionary
student_info.update({'name': 'meann', 'year': '4th',
                     'studentnumber': '2017-00212-MN-0'})
print(student_info)
