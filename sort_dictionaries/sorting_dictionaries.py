# List of dictionaries
dct = {'name': 'rom', 'address': 'tondo', 'course': 'bscoe', 'year': '4th'}
print('List of Dictionaries:\n', dct)
print()
# Sorted list of dictionaries
sort_dct = sorted(dct)
print('list of sorted dictionaries:\n', sort_dct)
print()

# Sort in descending order
sort_dct = sorted(dct, reverse=True)
print('list of sorted dictionaries in descending order:\n', sort_dct)
